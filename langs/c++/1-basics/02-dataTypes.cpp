#include <bits/stdc++.h>
using namespace std;

//INTEGER, LONG, LONG LONG

int main (){
    // int 
    int x;
    
    // long
    long y;
    
    // long long
    long long z;

    cin >> x >> y >> z;
    cout << x << " "<< y << " " << z << endl;
    return 0;
}

